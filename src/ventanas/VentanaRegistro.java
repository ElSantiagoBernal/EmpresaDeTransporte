    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty()
                    || txtIdentificacion.getText().isEmpty()
                    || cmbGenero.getSelectedItem().toString().equals("Genero")
                    || txtUsuarioCorreo.getText().isEmpty() || txtContrase침a.getText().isEmpty()
                    || txtConfirmacionContrase침a.getText().isEmpty() || txtNombre.getText().equals("Nombre/s")
                    || txtApellido.getText().equals("Apellido/s") || txtIdentificacion.getText().equals("Identificaci칩n")
                    || txtUsuarioCorreo.getText().equals("Usuario - correo")
                    || txtContrase침a.getText().equals("Contrase침a") || txtConfirmacionContrase침a.getText().equals("Confirmaci칩n contrase침a")) {
                throw new CampoVacioException();
            }
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            String id = txtIdentificacion.getText();
            String genero = cmbGenero.getSelectedItem().toString();

            String usuarioCorreo = txtUsuarioCorreo.getText();
            String contrase침a = txtContrase침a.getText();
            if (!contrase침a.equals(txtConfirmacionContrase침a.getText())) {
                JOptionPane.showMessageDialog(null, "Las contrase침as no coinciden.");
                return;
            }
            String rol = cmbRol.getSelectedItem().toString();

            if (rol.equals("Pasajero")) {
                Pasajero pasajero = new Pasajero(usuarioCorreo, contrase침a, nombre, apellido, id, genero);
                ventanaLogIn.controladorUsuario.registrarUsuario(pasajero);
                ventanaLogIn.controladorUsuario.a침adirPersonaRegistradaAdmin(ventanaLogIn.usuarioLogeado);
                JOptionPane.showMessageDialog(null, "Se registro el pasajero");
            } else if (rol.equals("Cajero")) {
                Cajero cajero = new Cajero(0, usuarioCorreo, contrase침a, nombre, apellido, id, genero);
                ventanaLogIn.controladorUsuario.registrarUsuario(cajero);
                ventanaLogIn.controladorUsuario.a침adirPersonaRegistradaAdmin(ventanaLogIn.usuarioLogeado);
                JOptionPane.showMessageDialog(null, "Se registro el cajero");

            } else if (rol.equals("Administrador")) {
                Administrador admin = new Administrador(0, usuarioCorreo, contrase침a, nombre, apellido, id, genero);
                ventanaLogIn.controladorUsuario.registrarUsuario(admin);
                ventanaLogIn.controladorUsuario.a침adirPersonaRegistradaAdmin(ventanaLogIn.usuarioLogeado);
                JOptionPane.showMessageDialog(null, "Se registro el administrador");
            } else {
                Despachador despachador = new Despachador(0, usuarioCorreo, contrase침a, nombre, apellido, id, genero);
                ventanaLogIn.controladorUsuario.registrarUsuario(despachador);
                JOptionPane.showMessageDialog(null, "Se registro el despachador");
                ventanaLogIn.controladorUsuario.a침adirPersonaRegistradaAdmin(ventanaLogIn.usuarioLogeado);
            }
            limpiarCampos();
        } catch (PersonaYaRegistradaException | UsuarioYaRegistradoException | CampoVacioException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

